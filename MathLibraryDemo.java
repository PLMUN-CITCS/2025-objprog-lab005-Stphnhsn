public class MathFunctions {
    public static void main(String[] args) {
        
        double powerResult = Math.pow(2, 3);

        
        double squareRootResult = Math.sqrt(16);

        
        long roundedResult = Math.round(5.6);

        
        double randomNumber = Math.random();

        
        System.out.println("Power: " + powerResult);
        System.out.println("Square Root: " + squareRootResult);
        System.out.println("Rounded Number: " + roundedResult);
        System.out.println("Random Number: " + randomNumber);
    }
}
